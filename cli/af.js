#!/usr/bin/env node
import { Command } from "commander";

import { importJson } from "../lib/importJson.js";

const appPkg = await importJson("../package.json");

const program = new Command();
program.version(appPkg.version);
program.command("deploy-cra", "Deploy a prebuilt CRA application generated by @olenbetong/react-scripts");
program.command("prepare-bundle", "Packs the current project and uploads it to Appframe bundles");
program.command(
  "publish-to-prod",
  "Publishes a deployed application, generates the transaction, and sends it to the production server (download only)"
);
program.command(
  "generate-data-object",
  "Generates a script to create a data API data object or procedure. (Tip: Pipe to clipboard)"
);

program.action(() => {
  program.help();
});
program.parse(process.argv);
